TASK5 SEGMENT Para Public 'Code'
ASSUME CS: TASK5
org 1000h

START:
MOV AL, 80H
OUT 1EH, AL

REPEAT:

    ; CLEAR
    MOV AL,00H
    OUT 18H,AL
    OUT 1AH,AL
	MOV AL,00H
    OUT 1CH,AL

    ; DELAY
	MOV CX,0FFFFH
	D0: LOOP D0

    ; COLUMN 1
	MOV AL, 093H ;1001 0011
	COUT 18H, AL
	COUT 1AH, AL
	MOV AL, 02H ; 0000 0010
	COUT 1CH, AL
	
	; DELAY
	MOV CX,0FFFFH
	D1: LOOP D1
	
	; COLUMN 2
	MOV AL, 07DH ;0111 1101
	COUT 18H, AL
	COUT 1AH, AL
	MOV AL, 04H ; 0000 0100
	COUT 1CH, AL
	
	; DELAY
	MOV CX,0FFFFH
	D2: LOOP D2
	
	; COLUMN 3
	MOV AL, 06DH ;0110 1101 
	COUT 18H, AL
	COUT 1AH, AL
	MOV AL, 08H ; 0000 1000
	COUT 1CH, AL
	
	; DELAY
	MOV CX,0FFFFH
	D3: LOOP D3
	
	; COLUMN 4
	MOV AL, 06DH ;0110 1101 same as 3
	COUT 18H, AL
	COUT 1AH, AL
	MOV AL, 010H ; 0001 0000
	COUT 1CH, AL
	
	; DELAY
	MOV CX,0FFFFH
	D4: LOOP D4
	
	; COLUMN 5
	MOV AL, 093H ;1001 0011 same as 1
	COUT 18H, AL
	COUT 1AH, AL
	MOV AL, 020H ; 0010 0000
	COUT 1CH, AL
	
	; DELAY
	MOV CX,0FFFFH
	D5: LOOP D5

JMP REPEAT

TASK5 ENDS
END START
